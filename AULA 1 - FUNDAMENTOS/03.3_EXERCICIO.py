# DESENVOLVA UM PROGRAMA QUE LEIA AS DUAS NOTAS
# DE UM ALUNO, CALCULE E MOSTE A SUA MEDIA


n1 = float(input("Digite a primeira nota: "))
n2 = float(input("Digite a segunda nota: "))

m = (n1 + n2)/2

print(f"O aluno tirou {n1:.1f} na primeira prova e {
      n2:.1f} na segunda prova,\nSua media final foi: {m:.1f} ")
